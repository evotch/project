<h4 id="radio-button">Radio button</h4>
<div class="callout callout-pink">
	<p>Do not use this term on the website.</p>
</div>
<p>Refers to <i>option button</i>, the round buttons in dialog boxes that users select to make one choice from among several items. Avoid saying “radio button” in end-user and technical documentation. Instead, use option button or option. To describe how a user makes one choice from among several items, use the term select rather than click or choose.</p>

