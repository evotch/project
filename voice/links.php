<h4 id="links">Links</h4>
<p>Use the following Do/Don't list when creating links:</p>
<div class="callout callout-green">
<h4>Do</h4>
<p>Use the term <i>select</i> to describe the action a user takes with reference to a link.<br>
Use title case on standalone link titles; use no title case on links inside a sentence. (e.g. standalone = Carry-On Baggage; sentence = For specific baggage dimensions, view our carry-on baggage page.)<br>
Use default style for links in a list<br>
Use full addresses or succinct descriptions of locations for link titles (i.e. <a href="#">www.alaskaairlines.com</a> not <a href="#">click here</a> to visit alaskaair.com)</p>
</div>
<div class="callout callout-pink">
<h4>Don't</h4>
<p>Include punctuation as part of link (see exceptions for freestanding links with exclamation points and question marks)<br>
Use quotes around links<br>
Use "Click Here" as a link label<br>
</p>
</div>