<h4 id="its-vs-its">Its vs. it's</h4>
<p>Its is the possessive pronoun of "it" and does not require an apostrophe. It’s is a conjunction for "it is." Only use an apostrophe if the conjunction could otherwise be expressed as "it is."</p>
<div class="callout callout-green">
	<h4>Correct</h4>
	<p>It's our responsibility to ensure a positive user experience. (We could say “it is” here.) Doing business over the Internet has its advantages. (We could not say "it is" here.)</p>
</div>