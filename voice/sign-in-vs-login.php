<h4 id="sign-in-s-login">Sign in vs. login</h4>
<p>Use the term <i>sign in</i> rather than <i>login</i> with EasyBiz and My Account. Use sign in as two words as a verb and hyphenate as a noun.</p>
<div class="callout callout-green">
	<h4>Correct</h4>
	<p>Verb: Sign in any time you like.<br>
	Noun: You can redeem your awards after sign-in.
</p>
