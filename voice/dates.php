<h4 id="dates">Dates</h4>
<p>Spell out dates in full. Use a comma after the year within a sentence. Do not use ordinal figures like "th" or "st" after days. Always include the year in customer facing communication.</p>
<div class="callout callout-green">
	<h4>Correct</h4>
	<p>March 21, 2003<br>We will meet on Thursday, March 21, 2003, to decide on the project parameters.<br>We will meet on March 21, 2003, to decide on the project parameters.<br>We will meet on March 21 2014.</p>
</div>
<h4 class="secondary"id="dates-abbreviating">Dates, abbreviating a series</h4>
<p>When abbreviating a series of dates, use the following format:</p>
<div class="callout callout-green">
	<h4>Correct</h4>
	<p>Jan. 5-6; Apr. 26-28; Aug. 8,11.<br>May 5-6; Jun. 1, 3-7; Oct. 4-5, 6, 16.</p>
</div>


