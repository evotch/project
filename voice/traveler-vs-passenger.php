<h4 id="traveler-vs-passenger">Traveler vs. Passenger</h4>
<p>When referring to a customer, use the term <i>traveler</i> rather than <i>passenger</i>. The term <i>customer</i> is also acceptable. Also, be aware of the distinction between the terms Traveler and Customer. Traveler is the person that gets onboard the flight, the Customer is the person buying the ticket. These are not always one and the same.</p>
