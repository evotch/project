<h4 id="open-jaw">Open Jaw</h4>
<p>This word is written as two words when used as a noun and is hyphenated when it is used as an adjective.</p>
<div class="callout callout-pink">
	<p>Avoid use in a customer facing way, unless needed. This is not a commonly understood term outside of the airline industry. </p>
</div>
<div class="callout callout-green">
	<p>Noun: This ticket has a segment that is open jaw.
Adjective: For some awards, open-jaw itineraries are not allowed.
</p>
</div>
