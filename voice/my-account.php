<h4 id="my-account">My Account</h4>
<p>My Account encompasses a traveler's personalized information at alaskaair.com. In text, it is acceptable to refer to a traveler's My Account as their My Account profile. See About My Account link for more background.
My Account encompasses a user's personalized information at alaskaair.com: Name and mailing address, email address, Mileage Plan number and activity, travel preferences such as cities and seating, credit card information, Discount Codes, wallet funds, tier status benefits, future trip itinerary information, and family/friend traveler profiles. 
