<h4>Capitalization&mdash;Hyphenated words </h4>
<p>Capitalization for hyphenated words varies depending upon where they are being used—within a sentence, to begin a sentence, or as a heading or title (see examples below). In hyphenated names of organizations and products, follow the organization’s style standards in each case.</p>
<div class="callout callout-green">
	<h4>Correct</h4>
	<p>Within a sentence: carry-on baggage<br>
Beginning a sentence: Carry-on baggage<br>
In Headings and Titles: Carry-On Baggage
</p>
</div>