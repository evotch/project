<h4 id="carry-on">Carry on vs. carry-on</h4>
<p>When describing baggage, use the two-word term <i>carry on</i> as a verb. Use the hyphenated term <i>carry-on</i> as an adjective.</p>
<div class="callout callout-green">
	<h4>Correct</h4>
	<p>Verb: We will carry on our bags instead of checking them.<br>Adjective: Carry-on baggage is restricted to one small bag and one personal item.</p>
</div>