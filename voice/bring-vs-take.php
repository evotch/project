<h4 id="bring-vs-take">Bring vs. take</h4>
<p>The proper use of bring vs. take depends upon the context of the statement. Bring implies coming to a person or place. Take implies going away from a person or place.</p>
<div class="callout callout-teal">
<h4>Correct</h4>
<p>How many bags may I bring? (if we mean bring <i>to the airport</i> or <i>on the airplane</i>)</p>
<p>How many bags may I take? (if we mean take <i>from the luggage carousel</i> or <i>from the store</i>)</p>
