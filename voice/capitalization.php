<h4 id="cancel">Capitalization</h4>
<p>Capitalize <i>north, south, east, and west</i> when they designate definite regions or are part of proper names:</p>
<div class="callout callout-green">
	<h4>Correct</h4>
	<p>the West Coast<br>in the Midwest<br>back East<br></p>
</div>
<h4 class="secondary">Capitalization&mdash;Headings</h4>
<p>Capitalize the first and last words in titles and subtitles and capitalize all other major words (nouns, pronouns, verbs, adjectives, adverbs, and some conjunctions. 
The words a, an, as, and, to, but, for, or, and nor are always lowercased.</p>
<div class="callout callout-green">
	<h4>Correct</h4>
	<p>What to Expect at the Airport<br>About Web Check-In<br>Double Miles at Outrigger Condos in Hawaii</p>
</div>
<h4 class="secondary">Capitalization&mdash;Hyphenated words </h4>
<p>Capitalization for hyphenated words varies depending upon where they are being used—within a sentence, to begin a sentence, or as a heading or title (see examples below). In hyphenated names of organizations and products, follow the organization’s style standards in each case.</p>
<div class="callout callout-green">
	<h4>Correct</h4>
	<p>Within a sentence: carry-on baggage<br>
Beginning a sentence: Carry-on baggage<br>
In Headings and Titles: Carry-On Baggage
</p>
</div>
<h4 class="secondary">Capitalization&mdash;Nouns </h4>
<p>Capitalize every proper noun, that is, the official name of a particular person, place, or thing:</p>
<div class="callout callout-green">
	<h4>Correct</h4>
	<p>the Internet<br>the World Wide Web<br>the U.S. Postal Service</p>
</div>