<h4 id="faa">FAA</h4>
<p>Federal Aviation Administration. When writing, spell it out in full whenever possible. If the term is used more than once within the same document, spell it out on the first mention and abbreviate on all subsequent mentions. If the term is required, but space restrictions will not allow you to spell it out in full, it is acceptable to use only the abbreviation since it is a highly recognizable term in the airline industry.</p>





