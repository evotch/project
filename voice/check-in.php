<h4 id="check-in">Check in/check-in/checked in</h4>
<p>This term is irregular and is treated differently depending upon which part of speech it is being used as. If the term is in a headline, use initial caps. For example: Now Faster Check-In!</p>
<div class="callout callout-teal">
	<h4>Correct</h4>
	<p>Verb: Check in any time you like. -or- They have already checked in.<br>Noun: No penalty for early check-in.<br>Adjective: Please note your check-in time.
</p>
