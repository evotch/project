<h4 id="state-province">State/province names</h4>
<p>British Columbia is abbreviated as B.C. When written with a city: Vancouver, B.C.<br>
Washington, D.C., is written using a comma and periods.<br>
United States: write out the name in full, unless there are restrictions on space. See <a href="#currency">currency</a> for U.S. dollar guidelines.<br>
</p>