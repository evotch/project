	<h4 class="secondary">In simple sentences</h4>
	<p>A sentence may contain one subject with a compound predicate, i.e., two predicates connected by a coordinating conjunction. In such sentences, no comma separates the two predicates.</p>
<div class="callout callout-green">
	<h4>Correct</h4>
	<p>This innovation saves time and speeds you through the airport.</p>
</div>


	<h4 class="secondary">In numbers</h4>
	<p>Use commas to separate the thousands place when formatting numbers.</p>
<div class="callout callout-green">
	<h4>Correct</h4>
	<p>10,000<br>5,000</p>
</div>

