<h4 id="one-way">One way</h4>
<p>This word is written as two words when used as a noun and is hyphenated when used as an adjective. For button text, the words should be hyphenated.</p>
<div class="callout callout-green">
	<p>Noun: The trip we reserved was only one way.<br>
Adjective: Use the online system to book your one-way ticket.
</p>
</div>
