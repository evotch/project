<h4 id="confirmation-code">Confirmation Code</h4>
<p>The 6-letter code that serves as a customer’s personal identifier when referencing their reservation. It’s the same thing as a record locator. You can have many passengers listed under one confirmation code or on one record locator. A record locator is used to access a PNR but isn’t a PNR itself. (See also <a href="#pnr">PNR</a>.)</p>

