<h4 id="record-locator">Record locator (reloc or PNR)</h4>
<div class="callout callout-pink">
	<p>Do not use. Use Confirmation Code instead</p>
</div>
<p>A customer's personal identifier to find their reservation. You can have many passengers listed on one Record Locator. Also called "PNR." A record locator is used to access a PNR but isn't a PNR itself. It's the same as a Confirmation Code. </p>


