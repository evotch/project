<h4 id="currency">Currency</h4>
<p>Several scenarios for correct currency style are listed below.</p>

<div class="callout callout-green">
	<h4>Correct</h4>
	USD (to abbreviate the United States dollar)<br>CAD (to abbreviate the Canadian dollar)<br>MXN (to abbreviate the Mexican peso)<br>EUR (to abbreviate the European euro)
</div>	


	<h4 class="secondary">In pricings or fare summaries</h4>
	<p>Only use currency designator (e.g. USD, CAD) on grand total in the following format: USD$491.50. Note: Where space is at a premium on certain screens, US instead of USD is acceptable.</p>



	<h4 class="secondary">In tables</h4>
	<p>Designate the entire table as one currency type with a beginning or end statement such as "All prices are quoted in U.S. dollars".</p>

	<h4 class="secondary">In text</h4>
	<p>Use periods in "U.S." and place currency designator (e.g. USD, CAD) in parentheses after the monetary value.</p>

