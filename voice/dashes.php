<h4 id="dashes">Dashes ( <span class="secondary">&amp;mdash; &amp;ndash; </span>)</h4>
<div class="callout callout-pink">
	<p>Do NOT use spaces on either side of the em dash, or en dash</p>
</div>

<p>Use en dashes (–) to connect numbers in a range and em dashes (—) with text rather than two hyphens (–). For phone numbers, use hyphens instead of en dashes. Allow no spaces on either side of an en dash. Do NOT use a space on either side of an em dash. Microsoft Word will auto-format an em dash after you enter two hyphens and continue typing. Note: If you do not have access to an en dash character you can substitute with a hyphen.</p>
<div class="callout callout-green">
	<h4>Correct</h4>
	<p>Our sale is on the week of December 7&ndash;13, 2003. (en dash)<br>
	Use 15,000&ndash;20,000 miles to upgrade this vacation package. (en dash)<br>
	1-800-ALASKAAIR (hyphens)<br>
	Get ready for an action-packed vacation&mdash;enter your bid today! (em dash)</p>
</div>